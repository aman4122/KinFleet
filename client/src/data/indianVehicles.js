export function filterVehicles({ budget, dailyKm, seating, bodyType }) {
  let filtered = [...indianVehicles];

  if (budget) {
    filtered = filtered.filter((v) => v.priceMin <= budget);
  }

  if (seating) {
    if (seating === '8+') {
      filtered = filtered.filter((v) => v.seating >= 8);
    } else {
      filtered = filtered.filter((v) => v.seating >= Number(seating));
    }
  }

  if (bodyType && bodyType !== 'All') {
    filtered = filtered.filter((v) => v.segment === bodyType || v.bodyType === bodyType);
  }

  if (dailyKm) {
    const km = Number(dailyKm);
    if (km < 40) {
      // Prefer Petrol, EV, CNG
      filtered.sort((a, b) => {
        const aScore = ['Petrol', 'Electric', 'CNG', 'Petrol+CNG'].includes(a.fuelType) ? 0 : 1;
        const bScore = ['Petrol', 'Electric', 'CNG', 'Petrol+CNG'].includes(b.fuelType) ? 0 : 1;
        return aScore - bScore;
      });
    } else if (km <= 70) {
      // Prefer CNG, Hybrid
      filtered.sort((a, b) => {
        const aScore = ['CNG', 'Hybrid', 'Petrol+CNG'].includes(a.fuelType) ? 0 : 1;
        const bScore = ['CNG', 'Hybrid', 'Petrol+CNG'].includes(b.fuelType) ? 0 : 1;
        return aScore - bScore;
      });
    } else {
      // Prefer Diesel, Hybrid
      filtered.sort((a, b) => {
        const aScore = ['Diesel', 'Hybrid'].includes(a.fuelType) ? 0 : 1;
        const bScore = ['Diesel', 'Hybrid'].includes(b.fuelType) ? 0 : 1;
        return aScore - bScore;
      });
    }
  }

  return filtered;
}
